import Foundation
class CalculadoraModelo{
    
    func sumar(_ numero1: Double, numero2: Double)->Double{
        return numero1+numero2
    }
    func restar(_ numero1: Double, numero2: Double)->Double{
        return numero1-numero2
    }
    func multiplicar(_ numero1: Double, numero2: Double)->Double{
        return numero1*numero2
    }
    func dividir(_ numero1: Double, numero2: Double)->Double{
        return numero1/numero2
    }
}
